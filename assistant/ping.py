from datetime import datetime

from . import asst_cmd


@asst_cmd(pattern="ping$", owner=True)
async def _(event):
    start = datetime.now()
    msg = await event.reply("Pong!")
    end = datetime.now()
    ms = (end - start).microseconds / 1000
    await msg.edit(f"**Pong!!**\n `{ms} ms`")
